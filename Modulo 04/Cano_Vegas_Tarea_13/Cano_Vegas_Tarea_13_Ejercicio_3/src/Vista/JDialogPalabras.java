    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        String palabra = txtPalabra.getText();
        int puntuacion = Integer.parseInt(txtPuntuacion.getText());
        
        objSentimientos.RegistrarPalabra(palabra, puntuacion);
        objSentimientos.ListarPalabras(tblPalabras);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int pos = tblPalabras.getSelectedRow();
        if (pos == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione una Palabra a editar");
            return;
        }

        try {
            String palabra = tblPalabras.getValueAt(pos, 0).toString();
            int puntuacion = Integer.parseInt(txtPuntuacion.getText());
            objSentimientos.EditarPuntuacion(palabra, puntuacion);
            objSentimientos.ListarPalabras(tblPalabras);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese datos validos");
        }

    }//GEN-LAST:event_btnEditarActionPerformed

