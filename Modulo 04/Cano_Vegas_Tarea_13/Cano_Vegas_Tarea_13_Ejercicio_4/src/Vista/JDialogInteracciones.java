    private void btnCalificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalificarActionPerformed
        int pos1 = tblUsuarios.getSelectedRow();
        int pos2 = tblPeliculas.getSelectedRow();
        
        if (pos1 == -1 || pos2 == -1) {
            JOptionPane.showMessageDialog(rootPane, "Seleccione un usuario y pelicula");
            return;
        }
        
        String idUsuario = tblUsuarios.getValueAt(pos1, 0).toString();
        String idPelicula = tblPeliculas.getValueAt(pos2,0).toString();
        int calificacion = Integer.parseInt(txtCalificacion.getText());
        
        objStreaming.AgregarCalificacion(idUsuario, idPelicula, calificacion);
        objStreaming.ListarUsuarios(tblUsuarios);
    }//GEN-LAST:event_btnCalificarActionPerformed

