    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int id = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        String tipo = txtTipo.getText();
        double precio = Double.parseDouble(txtPrecio.getText());

        objInstrumento = new Instrumento(id, nombre, tipo, precio);
        objArreglo_Instrumento.Agregar(objInstrumento);
        objArreglo_Instrumento.Listar(tblInstrumentos);
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int pos = tblInstrumentos.getSelectedRow();

        if (pos == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un Producto para eliminar");
            return;
        }

        int id = Integer.parseInt(tblInstrumentos.getValueAt(pos, 0).toString());
        objArreglo_Instrumento.Eliminar(id);
        objArreglo_Instrumento.Listar(tblInstrumentos);
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            ArrayList<Instrumento> resultados = objArreglo_Instrumento.buscarPorNombre(txtBusqueda.getText().trim());
            
            DefaultTableModel modelo = (DefaultTableModel) tblInstrumentos.getModel();

            if (resultados.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontraron coincidencias");
                ArrayList<Instrumento> todos = objArreglo_Instrumento.listarTodos();
                for (Instrumento inst : todos){
                    modelo.addRow(new Object[]{inst.getId(), inst.getNombre(), inst.getTipo(), inst.getPrecio()});
                }
            } else {
                for (Instrumento inst : resultados) {
                    modelo.setRowCount(0);
                    modelo.addRow(new Object[]{inst.getId(), inst.getNombre(), inst.getTipo(), inst.getPrecio()});
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Busque solo por nombres");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

