    private void btn_registrar_especialidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrar_especialidadActionPerformed
        // TODO add your handling code here:
        String codigo = spn_codigo.getValue().toString();
        String area = txt_area_especializada.getText();
        
        int pisoubicacion = (int) spn_piso.getValue();

        if (area == null || "".equals(area)
                || txt_costoconsulta.getText() == null
                || "".equals(txt_costoconsulta.getText())) {
            JOptionPane.showMessageDialog(null,
                    "Escriba los datos completos",
                    "Error", JOptionPane.WARNING_MESSAGE);
            txt_area_especializada.setText("");
            txt_costoconsulta.setText("");
            spn_codigo.setValue(1);
            spn_piso.setValue(0);
            return;
        }
        
        double costoconsulta;
        
        try {
            costoconsulta = Double.parseDouble(txt_costoconsulta.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Ingrese un número válio");
            return;
        }
        
        Especialidad objEspecialidad = new Especialidad(codigo, area, costoconsulta, pisoubicacion);

        objArreglos.registrarEspecialidad(objEspecialidad);
        objArreglos.ordenarEspecialidades(tbl_demuestra);
        actualizarComboEspecialidad();

        txt_area_especializada.setText("");
        txt_costoconsulta.setText("");
        spn_codigo.setValue(1);
        spn_piso.setValue(0);

        spn_codigo.requestFocus();


    }//GEN-LAST:event_btn_registrar_especialidadActionPerformed

    private void btn_registrar_medicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrar_medicoActionPerformed
        // TODO add your handling code here:
        try {
            String dnimedico = txt_dni_medico.getText();
            String nombre = txt_nombre_medico.getText();
            int aniosexp = (int) spn_aniosesperiencia.getValue();
            int horassemanales = (int) spn_jornada.getValue();
            int pos = cbo_especialidad.getSelectedIndex();
            
            if (pos == -1){
                JOptionPane.showMessageDialog(null, "Seleccione una especialidad");
                return;
            }

            if (dnimedico == null || dnimedico.isEmpty()
                    || nombre == null || nombre.isEmpty()
                    ) {

                JOptionPane.showMessageDialog(null,
                        "Escriba los datos completos",
                        "Error", JOptionPane.WARNING_MESSAGE);
                txt_dni_medico.setText("");
                txt_nombre_medico.setText("");
                spn_aniosesperiencia.setValue(0);
                spn_jornada.setValue(7);
                cbo_especialidad.setSelectedIndex(-1);
                txt_dni_medico.requestFocus();
                return;
            }
          
            Especialidad objEspecialidad = objArreglos.getEspecialidad(pos);

            Medico objMedico = new Medico(dnimedico, nombre, aniosexp, horassemanales, objEspecialidad);

            objArreglos.registrarMedico(objMedico);
            objArreglos.ordenarMedicosPorExp();
            actualizarComboMedico();
            objArreglos.ordenarMedicos(tbl_demuestra);

        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null,
                    "Escriba los datos completos",
                    "Error", JOptionPane.WARNING_MESSAGE);
        }

        txt_dni_medico.setText("");
        txt_nombre_medico.setText("");
        spn_aniosesperiencia.setValue(0);
        spn_jornada.setValue(7);
        cbo_especialidad.setSelectedIndex(-1);

        txt_dni_medico.requestFocus();


    }//GEN-LAST:event_btn_registrar_medicoActionPerformed

