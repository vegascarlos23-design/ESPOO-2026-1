    private void btn_registrar_citasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrar_citasActionPerformed

        int pos = cbo_medico.getSelectedIndex();
        if (pos == -1) {
            JOptionPane.showMessageDialog(null,
                    "Escriba los datos completos",
                    "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Medico objmedico = objArreglos.getMedico(pos);
        Especialidad especialidad = objmedico.getEspecialidad();
        String fecha = spn_fecha.getValue().toString();

        CitaMedica objCitaMedica = new CitaMedica(objmedico, especialidad, fecha);

        objArreglos.registrarCita(objCitaMedica, objmedico.getDniMedico(), objmedico.getEspecialidad().getCodEspecialidad());
        objArreglos.ordenarCitasPorMonto();
        objArreglos.ordenarCitas(tbl_demuestra);

        cbo_medico.setSelectedIndex(-1);
        cbo_medico.requestFocus();

    }//GEN-LAST:event_btn_registrar_citasActionPerformed

    private void cbo_medicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbo_medicoActionPerformed
        // TODO add your handling code here:
        int pos = cbo_medico.getSelectedIndex();
        
        if (pos == -1) return;
        
        Medico medico = objArreglos.getMedico(pos);
        txt_especialidad_cita.setText(medico.getEspecialidad().getNombreArea());
    }//GEN-LAST:event_cbo_medicoActionPerformed

