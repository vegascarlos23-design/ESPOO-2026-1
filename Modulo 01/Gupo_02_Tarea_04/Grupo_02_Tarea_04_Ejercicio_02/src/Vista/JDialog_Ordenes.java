    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        String ruc = cbo_proveedores.getSelectedItem().toString();
        int codigo = Integer.parseInt((cbo_componentes.getSelectedItem().toString().split("-")[0]));
        
        if (cbo_componentes.getSelectedItem() == null ||
                cbo_componentes.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this,
                    "Deben haber proveedores y componentes",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        int cantidad = (int) spn_cantidad.getValue();
        
        control.registrarOrden(ruc, codigo, cantidad);
        listarOrdenes();
    }//GEN-LAST:event_btn_registrarActionPerformed

