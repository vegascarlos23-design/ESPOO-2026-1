    public void cargarCombos(){
        cbo_proveedores.removeAllItems();
        cbo_componentes.removeAllItems();
        
        for (int i = 0; i < control.getCantidadProveedores(); i++){
            cbo_proveedores.addItem(control.getProveedor(i).getRucProveedor());
        }
        
        for (int i = 0; i < control.getCantidadComponentes(); i++){
            cbo_componentes.addItem(control.getComponente(i).getCodComponente() + "-" +
                    control.getComponente(i).getNombrePieza());
        }
    }
    
    public void listarOrdenes(){
        control.listarOrdenes(tbl_tabla);
    }

