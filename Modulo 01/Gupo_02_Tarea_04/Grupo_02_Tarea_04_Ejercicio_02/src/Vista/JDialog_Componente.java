    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        int codigo = (int)spn_codigo.getValue();
        String nombre = txt_nombre.getText();
        String marca = txt_marca.getText();
        double precio = (double)spn_precio.getValue();
        
        if (verificarDato(nombre) || verificarDato(marca)){
            JOptionPane.showMessageDialog(this, "Debe rellenar los campos",
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Componente objComponente = new Componente(codigo, nombre, marca, precio);
        control.registrarComponente(objComponente);
        actualizarDatos();
        refrescarDatos();
    }//GEN-LAST:event_btn_registrarActionPerformed

