    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            String Id = txtId.getText();
            int sillas = Integer.parseInt(txtSilla.getText());
            String equipamiento = txtEquipamiento.getText();
            
            SalaEstudio objSalaEstudio = new SalaEstudio(Id, sillas, equipamiento, false);
            objArregloBiblioteca.RegistrarSala(objSalaEstudio);
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese datos Validos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

