    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            String IsbnLibro = txtISBNlibro.getText();
            String IdSala = txtIdSala.getText();
            String nombreAlumno = txtNombreAlumno.getText();
            int diasPrestamo = Integer.parseInt(txtDiasPrestamo.getText());
            
            RegistroPrestamo objRegistroPrestamo = new RegistroPrestamo(nombreAlumno, diasPrestamo);
            objArregloBiblioteca.registrarPrestamo(objRegistroPrestamo, IsbnLibro, IdSala);
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese datos Validos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

