    private void btnMuckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuckActionPerformed
        String Dni = txtDni.getText();
        String nombre = txtNombree.getText();
        String cargo = txtCargo.getText();
        double sueldo = Double.parseDouble(txtSueldo.getText());
        String fechaInicio = txtFecha.getText();
        objEmpleado = new Empleado(Dni, nombre, cargo, sueldo, fechaInicio);
        
        objArreglo_Empleados.Agregar(objEmpleado);
        objArreglo_Empleados.Listar(tblEmpleado);
    }//GEN-LAST:event_btnMuckActionPerformed

