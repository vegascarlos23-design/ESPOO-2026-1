    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        int codigo = Integer.parseInt(txtCodigo.getText());
        String Nombre = txtNombre.getText();
        String Cargo = txtCategoria.getText();
        String Marca = txtMarca.getText();

        objComponente = new Componente(codigo, Nombre, Cargo, Marca);

        objArreglo_Componente.Agregar(objComponente);
        objArreglo_Componente.Listar(tblComponente);
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void txtFiltrarKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFiltrarKeyReleased
        String filtro = txtFiltrar.getText();
        objArreglo_Componente.Filtrar(tblComponente, filtro);
    }//GEN-LAST:event_txtFiltrarKeyReleased

    
