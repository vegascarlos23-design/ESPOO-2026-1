    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            String dni = txtDni.getText();
            String nombre = txtNombre.getText();
            double presion = Double.parseDouble(txtPresion.getText());
            double temperatura = Double.parseDouble(txtTemperatura.getText());
            double saturacion = Double.parseDouble(txtSaturacion.getText());
            
            objPaciente = new Paciente(dni, nombre, presion, temperatura, saturacion,esp);
            objArregloEspecialidad.RegistrarPaciente(objPaciente, esp);
            dispose();
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese datos Validos");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

